        }
    }

    private fun dockingPoints(incomingList: List<GeoPoint>): List<GeoPoint> {
        val list = incomingList.toMutableList()
        var nextIndex: Int
        var center: GeoPoint
        var target: GeoPoint
        loop@ for (currentIndex in 1 until list.size step 2) {
            nextIndex = currentIndex + 1
            if (nextIndex >= list.size) break@loop
            center = list[currentIndex]
            target = list[nextIndex]
            if (center != target) {
                Collections.swap(list, nextIndex, nextIndex + 1)
                target = list[nextIndex]
                if (center != target) {
                    Log.e(
                        "TestData",
                        "dockingPoints: center not equals target: index = $currentIndex; nextIndex = $nextIndex; \n center = $center; target = $target "
                    )
                }
            }
        }

        return list.toList()
    }

